// Soma

function somar(number1, number2){
  return number1 + number2
}

const resultadoSoma = somar(2 , 3)
console.log('A soma dos dois números é: ' + resultadoSoma)


// Subtração

function subtrair(number1, number2){
  return number1 - number2
}

const resultadoSubtracao = subtrair(5 , 1)
console.log('A subtração dos dois números é: ' + resultadoSubtracao)

// Multiplicação

function multiplicacao(number1, number2){
  return number1 * number2
}

const resultadoMultiplicacao = multiplicacao(8 , 4)
console.log('A multiplicação dos dois números é: ' + resultadoMultiplicacao)



function divisao(number1, number2){
  return number1 / number2
}

const resultadoDivisao = divisao(9 , 3)
console.log('A divisão dos dois números é: ' + resultadoDivisao)


// Resto 

function resto(number1, number2){
  return number1 % number2
}

const resultadoresto = resto(11 , 3)
console.log('O resto da divisão dos dois números é: ' + resultadoresto)